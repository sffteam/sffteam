	public function index(){
		$products = Malls::find('all',array(
//			'order'=>array('Code'=>array('ASC'=>1))
		));
		$AllProducts = array();
		foreach($products as $p){
			array_push($AllProducts,array(
				'Code'=>$p['Code'],
				'Name'=>$p['Name'],
				'MRP'=>$p['MRP'],
				'DP'=>$p['DP'],
				'BV'=>$p['BV'],
				'PV'=>$p['PV'],
			));
		}
		return $this->render(array('json' => array("success"=>"Yes",'products'=>$AllProducts)));		
		
	}
